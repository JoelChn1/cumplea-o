function getURLParam(name) {
  const url = new URL(window.location.href);
  return url.searchParams.get(name);
}

function showDedicationText() { //seguidores
  let text = getURLParam('text');
  if (!text) {
    text = `La felicidad est√° en las peque√±as cosas: en los momentos compartidos, en las risas y en los gestos simples que nos llenan de vida.

Hoy celebramos tu cumplea√±os y todos esos detalles que te hacen especial, los que iluminan a quienes te rodean y hacen del mundo un lugar m√°s bonito.

Que se cumplan todas tus metas, que nunca te falten motivos para sonre√≠r y para seguir so√±ando.`;  } else {
    text = decodeURIComponent(text).replace(/\\n/g, '\n');
  }
  const container = document.getElementById('dedication-text');
  container.classList.add('typing');
  let i = 0;
  function type() {
    if (i <= text.length) {
      container.textContent = text.slice(0, i);
      i++;
      setTimeout(type, text[i - 2] === '\n' ? 350 : 45);
    } else {
      // Al terminar el typing, mostrar la firma animada
      setTimeout(showSignature, 600);
    }
  }
  type();
}

